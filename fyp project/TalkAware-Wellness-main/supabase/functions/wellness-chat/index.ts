import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are TalkAware, an empathetic AI wellness companion designed to detect early signs of mental wellness changes through conversation. Your role is to:

1. Engage in natural, supportive conversation
2. Analyze the user's communication patterns, tone, word choice, and sentiment
3. Look for signs of stress, anxiety, low mood, or emotional distress
4. After gathering enough context (usually 3-5 exchanges), provide a gentle wellness assessment

Guidelines:
- Be warm, understanding, and non-judgmental
- Ask open-ended questions about how they're feeling, their day, sleep, relationships, or work
- Never diagnose medical conditions - you are NOT a mental health professional
- If you detect concerning patterns, gently suggest speaking with a professional
- After enough conversation, summarize your observations in a caring way

When providing an assessment, format it like this:
ðŸŒ¿ **Wellness Check Summary**
- **Emotional State**: (your observation)
- **Stress Indicators**: (what you noticed)
- **Wellness Score**: (Low/Moderate/Good/Excellent)
- **Gentle Suggestions**: (1-2 supportive recommendations)

Remember: You're here to listen, support, and help users become more aware of their mental wellness - not to treat or diagnose.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing wellness chat with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Wellness chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
